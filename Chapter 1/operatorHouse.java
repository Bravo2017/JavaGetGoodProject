/**
 *P1.9 Write a program that prints a house using
 * +, -, | operators.
 */
public class operatorHouse
{
  public static void main(String[] args)
  {
    System.out.println("   +   ");
    System.out.println("  + +  ");
    System.out.println(" +   + ");
    System.out.println("+-----+");
    System.out.println("| .-. |");
    System.out.println("| | | |");
    System.out.println("+-+-+-+");
  }
}
