/**
 * P1.8 Write a program that prints an imitation of 
 * a Piet Mondrian painting. Use character sequences
 * such as @@@ or ::: to indicate different colors and
 * use - or | to form lines.
 */
public class PietMondrianPainting
{
  public static void main(String[] args)
  {
    System.out.println("  |@@@@@@@@@|");
    System.out.println("__|@@@@@@@@@|");
    System.out.println("  |@@@@@@@@@|");
    System.out.println("  |@@@@@@@@@|");
    System.out.println("  |@@@@@@@@@|");
    System.out.println("  |@@@@@@@@@|");
    System.out.println("__|_________|");
    System.out.println("::|       |_|");
    System.out.println("::|       | |");
    System.out.println("__|_______|_|");
  }
}

                         