/**
 * P1.12 Write a program that prints a poem of your choice.
 */
public class Invictus
{
  public static void main(String[] args)
  {
    System.out.println("Invictus");
    System.out.println();
    System.out.println("Out of the night that covers me,");
    System.out.println("Black as the Pit from pole to pole,");
    System.out.println("I thank whatever gods may be");
    System.out.println("For my unconquerable soul.");
    System.out.println();
    System.out.println("In the fell clutch of circumstance");
    System.out.println("I have not winced nor cried aloud.");
    System.out.println("Under the bludgeonings of chance");
    System.out.println("My head is bloody, yet unbowed.");
    System.out.println();
    System.out.println("Beyond this place of wrath and tears");
    System.out.println("Looms but the Horror of the shade,");
    System.out.println("And yet the menace of the years");
    System.out.println("Finds, and shall find, me unafraid.");
    System.out.println();
    System.out.println("It matters not how strait the gate,");
    System.out.println("How charged of punishments the scrolls.");
    System.out.println("I am the master of my fate: ");
    System.out.println("I am the captain of my soul.");
    System.out.println("William Ernest Henley");
  }
}
